//  argument vs paramitter

function sum (a, b) {
    const result = a + b;
    return result;
}
// console.log(sum);

const num1 = 5;
const num2 = 510;

const outpute = sum (num1, num2);

console.log(outpute);

// function এর ভেতর  ( a, b ) কে বলা হয় paramitter এবং output এর ( num1, num2) কে বলা হয় function এর argument
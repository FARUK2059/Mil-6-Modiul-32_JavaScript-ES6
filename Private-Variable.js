// avaScript এর প্রাইভেট ভেরিয়েবল হলো এমন ভেরিয়েবল যা কেবল একটি স্কোপ বা ফাংশনের আওতাধীনে সাধারণভাবে অ্যাক্সেস করা যায় এবং বাহ্যিক ফাংশন থেকে অ্যাক্সেস করা যায় না। এই ভেরিয়েবলগুলি কোনও প্রাইভেট ভেরিয়েবলের সংরক্ষণ করে তাদের অন্তর্ভুক্ত ফাংশনের মাধ্যমে অ্যাক্সেস এবং আপডেট করা যায়।

// একটি উদাহরণ দেখা যাক:

function Counter() {
    // এই ভেরিয়েবল প্রাইভেট হিসেবে সংরক্ষিত
    let count = 0;

    // প্রাইভেট ভেরিয়েবলের মান আপডেট করতে প্রাইভেট মেথড
    function increment() {
        count++;
    }

    // প্রাইভেট ভেরিয়েবলের মান প্রিন্ট করতে প্রাইভেট মেথড
    function showCount() {
        console.log(count);
    }

    // পাবলিক মেথডগুলি যা প্রাইভেট ভেরিয়েবলের মানের প্রবেশ ও পরিবর্তনের অনুমতি দেয়
    return {
        increase: function() {
            increment();
        },
        display: function() {
            showCount();
        }
    };
}

// Counter ফাংশনের একটি নতুন অবজেক্ট তৈরি করা
let counter1 = Counter();

// পাবলিক মেথড ব্যবহার করা
counter1.increase();
counter1.display(); // Output: 1

// এখন count ভেরিয়েবলের অ্যাক্সেস নিষিদ্ধ
console.log(counter1.count); // Output: undefined


// উপরের উদাহরণে, Counter ফাংশনের ভিতরে লুকিয়ে রয়েছে count নামক প্রাইভেট ভেরিয়েবল। এই ভেরিয়েবলের মান আপডেট এবং প্রিন্ট করার জন্য ব্যবহৃত হয় increment এবং showCount প্রাইভেট মেথড। পাবলিক মেথডগুলির মাধ্যমে কেবল আমরা প্রাইভেট ভেরিয়েবলের মানের প্রবেশ এবং পরিবর্তন করতে পারি। এই ভেরিয়েবল বাহ্যিকভাবে অ্যাক্সেস করা যায় না। এক্ষেত্রে, প্রাইভেট ভেরিয়েবলের মান একমাত্র মেথড এর মাধ্যমেই পরিবর্তন করা যায়।
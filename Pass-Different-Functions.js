// JavaScript-এ, আমরা একটি ফাংশনকে অন্য একটি ফাংশনের প্যারামিটার হিসেবে পাঠাতে পারি। এটি দ্বারা বিভিন্ন ফাংশনালিটি এবং প্রোগ্রামিং প্যাটার্ন সমর্থন করা হয়।

// একটি উদাহরণ দেখা যাক:

// দুটি ফাংশন যা সংজ্ঞায়িত করা হয়েছে
function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

// ফাংশনকে প্যারামিটার হিসেবে পাস করা
function calculate(operation, num1, num2) {
    return operation(num1, num2);
}

// প্যারামিটার হিসেবে দুটি ফাংশন পাস করা
console.log(calculate(add, 5, 3)); // Output: 8
console.log(calculate(subtract, 10, 4)); // Output: 6


// উপরের উদাহরণে, আমরা add এবং subtract ফাংশনগুলি সংজ্ঞায়িত করেছি। তারপরে, আমরা calculate ফাংশনের মাধ্যমে এই দুটি ফাংশনকে প্যারামিটার হিসেবে পাস করেছি। calculate ফাংশন প্যারামিটার হিসেবে একটি অপারেশন ফাংশন, একটি সংখ্যা (num1) এবং আরেকটি সংখ্যা (num2) নেয়। তারপরে, এই অপারেশন ফাংশনটি কল করা হয় উভয় সংখ্যা প্যারামিটারের সাথে।

// যেমন, calculate(add, 5, 3) কল করা হয়েছে এবং এটি add ফাংশনের সাথে 5 এবং 3 সংখ্যাগুলি কল করে। এর ফলাফল হলো 8। অতএব, calculate(subtract, 10, 4) কল করা হয়েছে এবং এটি subtract ফাংশনের সাথে 10 এবং 4 সংখ্যাগুলি কল করে। এর ফলাফল হলো 6।

// এই উদাহরণে, আমরা একটি ফাংশনকে অন্য একটি ফাংশনের প্যারামিটার হিসেবে পাস করে দেখতে পাচ্ছি। এটি প্রোগ্রামিংয়ে বিভিন্ন প্যাটার্নের উপর ভিত্তি করে, যেমন কলব্যাক ফাংশন, হাইয়ার অর্ডার ফাংশন, ফাংশনাল প্রোগ্রামিং ইত্যাদি।